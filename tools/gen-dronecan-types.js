const fs = require("fs");
const path = require("path");

const specPath = process.argv[2] || path.join(__dirname, "../agent-tools-spec.txt");
const outPath = path.join(__dirname, "../JS/core/dronecan-standard-types.js");

if (!fs.existsSync(specPath)) {
  console.error("Spec file missing:", specPath);
  process.exit(1);
}

const text = fs.readFileSync(specPath, "utf8");
const lines = text.split(/\r?\n/);
const types = [];
let current = null;

for (const line of lines) {
  const heading = line.match(/^### `(.+)`$/);
  if (heading) {
    current = { short: heading[1].replace(/\\_/g, "_") };
    continue;
  }
  const full = line.match(/^Full name: `(.+)`$/);
  if (full && current) {
    current.full = full[1].replace(/\\_/g, "_");
    continue;
  }
  const dt = line.match(/^Default data type ID: (\d+)/);
  if (dt && current?.full) {
    types.push({ id: Number(dt[1]), short: current.short, full: current.full });
    current = null;
  }
}

const byId = new Map();
for (const item of types) {
  if (!byId.has(item.id)) byId.set(item.id, item);
}

const sorted = [...byId.entries()].sort((a, b) => a[0] - b[0]);
const body = sorted.map(([id, t]) => `  ${id}: { shortName: ${JSON.stringify(t.short)}, fullName: ${JSON.stringify(t.full)} }`).join(",\n");

const output = `(function initDronecanStandardTypes(global) {
  /** Autogenerated from DroneCAN spec §7 — Default data type ID → DSDL full name */
  const standardTypesById = {
${body}
  };

  const serviceTypesById = {
    1: { shortName: "GetNodeInfo", fullName: "uavcan.protocol.GetNodeInfo" },
    2: { shortName: "GetDataTypeInfo", fullName: "uavcan.protocol.GetDataTypeInfo" },
    4: { shortName: "GetTransportStats", fullName: "uavcan.protocol.GetTransportStats" },
    5: { shortName: "GlobalTimeSync", fullName: "uavcan.protocol.GlobalTimeSync" },
    6: { shortName: "RestartNode", fullName: "uavcan.protocol.RestartNode" },
  };

  function categoryFor(fullName) {
    if (!fullName) return "Unknown";
    if (fullName.includes(".protocol.")) return "Protocol";
    if (fullName.includes(".equipment.power")) return "Power";
    if (fullName.includes(".equipment.")) return "Equipment";
    if (fullName.startsWith("ardupilot.")) return "ArduPilot";
    if (fullName.includes("dynamic_node_id")) return "Allocation";
    return "Standard";
  }

  function lookup(dataTypeId, isService) {
    const id = Number(dataTypeId);
    if (!Number.isFinite(id)) return null;
    const table = isService ? serviceTypesById : standardTypesById;
    const hit = table[id];
    if (!hit) {
      if (id >= 20000 && id < 21000) {
        return {
          shortName: \`Vendor_\${id}\`,
          fullName: \`vendor.message.\${id}\`,
          category: "Vendor",
          dataTypeId: id,
        };
      }
      return {
        shortName: \`Type_\${id}\`,
        fullName: \`unknown.message.\${id}\`,
        category: "Unknown",
        dataTypeId: id,
      };
    }
    return {
      ...hit,
      category: categoryFor(hit.fullName),
      dataTypeId: id,
    };
  }

  global.DRONECAN_STANDARD_TYPES = {
    standardTypesById,
    serviceTypesById,
    lookup,
    categoryFor,
  };
})(window);
`;

fs.writeFileSync(outPath, output, "utf8");
console.log(`Wrote ${sorted.length} message types to ${outPath}`);
